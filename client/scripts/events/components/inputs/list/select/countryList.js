Template.countryList.onRendered(function () {
  $('.countrySelect').select2({
    placeholder: 'Tips: you can type or select a country',
    allowClear: true
  });
});
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Template.countryList.events({
  "blur .countrySelect": function(event, template){
    let country= $('.countrySelect').select2("val");
    if(country===""){
      Bert.alert("You haven't selected any country!", "warning");
    }
  }
});
