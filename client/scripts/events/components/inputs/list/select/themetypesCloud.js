Template.themetypesCloud.onRendered(function () {

  $('.themetypesCloud').select2({
    placeholder: 'Tips: you can type or select themetypes',
    allowClear: true
  });
});
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Template.themetypesCloud.events({
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  "blur .themetypesCloud": function(event, template){
    let tags= $('.themetypesCloud').select2("val");
    console.log(tags);
    if($.trim(tags)===""){
      Bert.alert("You haven't selected any themetypes!", "warning");
    }
  }
});
