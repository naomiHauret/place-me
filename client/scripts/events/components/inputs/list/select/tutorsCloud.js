Template.tutorsCloud.onRendered(function () {
  $('.tutorsCloud').select2({
    placeholder: 'Tips: you can type or select tutors',
    allowClear: true
  });
});
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Template.tutorsCloud.events({
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  "blur .tutorsCloud": function(event, template){
    let tutors= $('.tutorsCloud').select2("val");

    if($.trim(tutors)===""){
      Bert.alert("You haven't selected any tutor!", "warning");
    }
  }
});
